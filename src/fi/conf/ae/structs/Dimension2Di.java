package fi.conf.ae.structs;

public class Dimension2Di implements Cloneable {
	
	private int width;
	private int height;
	
	public Dimension2Di(int width, int height) {
		setWH(width, height);
	}
	
	public void setWH(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
	
	@Override
	public Dimension2Di clone() {
		return new Dimension2Di(width, height);
	}
	
}
